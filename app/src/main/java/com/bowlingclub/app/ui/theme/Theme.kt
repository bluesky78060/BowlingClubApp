/**
 * BowlingClubTheme - Main theme for the Bowling Club app
 *
 * Features:
 * - Professional blue primary color (bowling alley atmosphere)
 * - Orange secondary accent (bowling ball)
 * - Full Material3 support
 * - Light/Dark theme support
 * - Custom bowling-specific colors for rankings, gender, scores
 *
 * @param darkTheme Whether to use dark theme (defaults to system setting)
 * @param content The composable content to apply the theme to
 */
@Composable
fun BowlingClubTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) {
        DarkColorScheme
    } else {
        LightColorScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = BowlingTypography,
        shapes = BowlingShapes,
        content = content
    )
}
